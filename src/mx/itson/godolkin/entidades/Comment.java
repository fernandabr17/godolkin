package mx.itson.godolkin.entidades;

import java.util.Date;

/**
 * Getters and setters de los comentarios de los posts.
 * @author Fernanda Barr√≥n
 */
public class Comment {
    
    private String body;
    private User user;
    private Date date;

    /**
     * @return the body
     */
    public String getBody() {
        return body;
    }

    /**
     * @param body the body to set
     */
    public void setBody(String body) {
        this.body = body;
    }

    /**
     * @return the user
     */
    public User getUser() {
        return user;
    }

    /**
     * @param user the user to set
     */
    public void setUser(User user) {
        this.user = user;
    }

    /**
     * @return the date
     */
    public Date getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(Date date) {
        this.date = date;
    }
    
}
